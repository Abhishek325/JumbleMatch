    public boolean SortAndCompare(String word, String wd)
    {  
        char[] sorted_word=sortString(word);
        char[] sorted_wd=sortString(wd);
        for(int i=0;i<word.length();i++)
        {
            if(sorted_word[i]!=sorted_wd[i]) 
                return false; 
        } 
        return true;
    }

    public char[] sortString(String text) 
    {
      char[] temp=text.toLowerCase().toCharArray();
      for(int i=0;i<text.length();i++)
      { 
        for(int j=0;j<=i;j++)
        { 
          if(temp[j]>temp[i]) 
          {
            char tempVar;
            tempVar=temp[i] ;
            temp[i]=temp[j];
            temp[j]=tempVar;  
          }
        }      
      } 
      return temp;
    }     
